"""Tests for `todolist-sorter init`."""
from pathlib import Path

import pytest
from click.testing import CliRunner

from app.cli import cli


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

_PROMPTS = "\n".join([
    "my-client-id",          # Todoist Client ID
    "my-client-secret",      # Todoist Client Secret
    "my-api-token",          # Todoist personal API token
    "",                      # LLM model (accept default)
    "my-llm-key",            # LLM API key
    "",                      # Database URL (accept default)
    "",                      # Debounce seconds (accept default)
    "",                      # Suppression window (accept default)
]) + "\n"


# ---------------------------------------------------------------------------
# Basic happy path
# ---------------------------------------------------------------------------


def test_init_creates_env_file(tmp_path):
    output = tmp_path / ".env"
    runner = CliRunner()
    result = runner.invoke(
        cli,
        ["init", "--output", str(output)],
        input=_PROMPTS,
    )
    assert result.exit_code == 0, result.output
    assert output.exists()


def test_init_env_contains_expected_keys(tmp_path):
    output = tmp_path / ".env"
    runner = CliRunner()
    result = runner.invoke(
        cli,
        ["init", "--output", str(output)],
        input=_PROMPTS,
    )
    assert result.exit_code == 0
    content = output.read_text(encoding="utf-8")
    for key in (
        "TODOIST_CLIENT_ID",
        "TODOIST_CLIENT_SECRET",
        "TODOIST_API_TOKEN",
        "LLM_MODEL",
        "LLM_API_KEY",
        "APP_API_KEY",
        "DATABASE_URL",
        "DEFAULT_DEBOUNCE_SECONDS",
        "SUPPRESSION_WINDOW_SECONDS",
    ):
        assert key in content, f"Expected {key} in generated .env"


def test_init_app_api_key_is_auto_generated_and_long(tmp_path):
    output = tmp_path / ".env"
    runner = CliRunner()
    result = runner.invoke(
        cli,
        ["init", "--output", str(output)],
        input=_PROMPTS,
    )
    assert result.exit_code == 0
    content = output.read_text(encoding="utf-8")
    # Parse out APP_API_KEY
    for line in content.splitlines():
        if line.startswith("APP_API_KEY="):
            key_val = line.split("=", 1)[1]
            assert len(key_val) >= 32, f"APP_API_KEY too short: {key_val!r}"
            break
    else:
        pytest.fail("APP_API_KEY not found in generated .env")


