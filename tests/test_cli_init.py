def test_init_env_is_parseable_as_dotenv(tmp_path):
    """Each non-comment line must be KEY=value."""
    output = tmp_path / ".env"
    runner = CliRunner()
    result = runner.invoke(
        cli,
        ["init", "--output", str(output)],
        input=_PROMPTS,
    )
    assert result.exit_code == 0
    for line in output.read_text(encoding="utf-8").splitlines():
        if not line or line.startswith("#"):
            continue
        assert "=" in line, f"Non-parseable line: {line!r}"


# ---------------------------------------------------------------------------
# Overwrite / --force
# ---------------------------------------------------------------------------


def test_init_existing_file_prompts_confirm(tmp_path):
    output = tmp_path / ".env"
    output.write_text("OLD=value\n", encoding="utf-8")
    runner = CliRunner()
    # Say "n" to overwrite prompt → abort
    result = runner.invoke(
        cli,
        ["init", "--output", str(output)],
        input="n\n",
    )
    assert result.exit_code != 0
    # File should remain unchanged
    assert output.read_text(encoding="utf-8") == "OLD=value\n"


def test_init_existing_file_confirm_yes_overwrites(tmp_path):
    output = tmp_path / ".env"
    output.write_text("OLD=value\n", encoding="utf-8")
    runner = CliRunner()
    # Say "y" first (overwrite prompt), then supply all prompts
    result = runner.invoke(
        cli,
        ["init", "--output", str(output)],
        input="y\n" + _PROMPTS,
    )
    assert result.exit_code == 0
    assert "TODOIST_CLIENT_ID" in output.read_text(encoding="utf-8")


def test_init_force_flag_overwrites_without_prompt(tmp_path):
    output = tmp_path / ".env"
    output.write_text("OLD=value\n", encoding="utf-8")
    runner = CliRunner()
    result = runner.invoke(
        cli,
        ["init", "--output", str(output), "--force"],
        input=_PROMPTS,
    )
    assert result.exit_code == 0
    content = output.read_text(encoding="utf-8")
    assert "TODOIST_CLIENT_ID" in content
    assert "OLD=value" not in content


